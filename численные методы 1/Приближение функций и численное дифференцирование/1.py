"""
Задаю начальное значение x = 0.1 и точность e = 0.0001

Использую итеративный метод - каждый следующий член ряда вычисляем через предыдущий

Вычисляю коэффициент связи: q = x / (8k² + 12k + 4)

Накапливаю сумму, пока отношение текущего члена к сумме превышает заданную точность

Вывожу промежуточные результаты на каждой итерации
"""

x = 0.1
e = 0.0001
k = 0
a = x
s = x
while (abs(a / s)) > e:
    q = x / (8 * k ** 2 + 12 * k + 4)
    a *= q
    s += a
    k += 1
    print("S = ", s, "k = ", k)
